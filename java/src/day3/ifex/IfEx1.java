package day3.ifex;

public class IfEx1 {

	public static void main(String[] args) {
		//정수 num가 0이면 0입니다라고 출력하는 코드를 작성하세요.
		//정수 num가 0이다
		//정수 num가 0과 같다
		int num = 10;
		if(num == 0) {
			//0입니다라고 출력;
			System.out.println("0 is 0.");
		}
		//정수 num가 0이 아니면 num is not 0이라고 출력하는 코드를 작성하시오.
		//num가 0이 아니다
		//num가 0이 같지 않다
		if(num != 0) {
			System.out.println(num + " is not 0");
		}
	}

}
